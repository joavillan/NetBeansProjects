package momatools.model.creation;

public class InformacionComunicaciones {
	private String puertoOptico;
	private String puertoPLC;
	private String puertoSerie;
	private String puertoRadioFrequencia;
	private String puertoMovil;
	private String puertoEthernet;
	public String getPuertoOptico() {
		return puertoOptico;
	}
	public void setPuertoOptico(String puertoOptico) {
		this.puertoOptico = puertoOptico;
	}
	public String getPuertoPLC() {
		return puertoPLC;
	}
	public void setPuertoPLC(String puertoPLC) {
		this.puertoPLC = puertoPLC;
	}
	public String getPuertoSerie() {
		return puertoSerie;
	}
	public void setPuertoSerie(String puertoSerie) {
		this.puertoSerie = puertoSerie;
	}
	public String getPuertoRadioFrequencia() {
		return puertoRadioFrequencia;
	}
	public void setPuertoRadioFrequencia(String puertoRadioFrequencia) {
		this.puertoRadioFrequencia = puertoRadioFrequencia;
	}
	public String getPuertoMovil() {
		return puertoMovil;
	}
	public void setPuertoMovil(String puertoMovil) {
		this.puertoMovil = puertoMovil;
	}
	public String getPuertoEthernet() {
		return puertoEthernet;
	}
	public void setPuertoEthernet(String puertoEthernet) {
		this.puertoEthernet = puertoEthernet;
	}
	
	
	
}
