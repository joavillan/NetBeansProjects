package momatools.model.creation;

public class Precinto {
	private String tipoPrecinto;
	private String numSeriePrecinto;
	private String codigoBIDIprecinto;
	private String color;
	private String fechaPrecinto;
	public String getTipoPrecinto() {
		return tipoPrecinto;
	}
	public void setTipoPrecinto(String tipoPrecinto) {
		this.tipoPrecinto = tipoPrecinto;
	}
	public String getNumSeriePrecinto() {
		return numSeriePrecinto;
	}
	public void setNumSeriePrecinto(String numSeriePrecinto) {
		this.numSeriePrecinto = numSeriePrecinto;
	}
	public String getCodigoBIDIprecinto() {
		return codigoBIDIprecinto;
	}
	public void setCodigoBIDIprecinto(String codigoBIDIprecinto) {
		this.codigoBIDIprecinto = codigoBIDIprecinto;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getFechaPrecinto() {
		return fechaPrecinto;
	}
	public void setFechaPrecinto(String fechaPrecinto) {
		this.fechaPrecinto = fechaPrecinto;
	}
			
		
}
