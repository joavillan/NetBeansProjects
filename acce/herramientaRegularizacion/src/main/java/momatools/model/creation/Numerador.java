package momatools.model.creation;

public class Numerador {
	private String codigoTipoConsumo;
	private String valorLectura;
	private String indRelevencia;
	public String getCodigoTipoConsumo() {
		return codigoTipoConsumo;
	}
	public void setCodigoTipoConsumo(String codigoTipoConsumo) {
		this.codigoTipoConsumo = codigoTipoConsumo;
	}
	public String getValorLectura() {
		return valorLectura;
	}
	public void setValorLectura(String valorLectura) {
		this.valorLectura = valorLectura;
	}
	public String getIndRelevencia() {
		return indRelevencia;
	}
	public void setIndRelevencia(String indRelevencia) {
		this.indRelevencia = indRelevencia;
	}			
		
}
