package momatools.model.creation;

public class Intensidades {
	private String intensidadPrimaria;
	private String intensidadSecundaria;
	private String intensidadTransfo;
	public String getIntensidadPrimaria() {
		return intensidadPrimaria;
	}
	public void setIntensidadPrimaria(String intensidadPrimaria) {
		this.intensidadPrimaria = intensidadPrimaria;
	}
	public String getIntensidadSecundaria() {
		return intensidadSecundaria;
	}
	public void setIntensidadSecundaria(String intensidadSecundaria) {
		this.intensidadSecundaria = intensidadSecundaria;
	}
	public String getIntensidadTransfo() {
		return intensidadTransfo;
	}
	public void setIntensidadTransfo(String intensidadTransfo) {
		this.intensidadTransfo = intensidadTransfo;
	}	
	
}
