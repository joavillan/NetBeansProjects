package momatools.model.creation;

public class DatosVisita {
	private String fechaResultadoVisita;
	private String resultadoVisita;
	private String defectosResultadoVisita;

	public String getFechaResultadoVisita() {
		return fechaResultadoVisita;
	}

	public void setFechaResultadoVisita(String fechaResultadoVisita) {
		this.fechaResultadoVisita = fechaResultadoVisita;
	}

	public String getResultadoVisita() {
		return resultadoVisita;
	}

	public void setResultadoVisita(String resultadoVisita) {
		this.resultadoVisita = resultadoVisita;
	}

	public String getDefectosResultadoVisita() {
		return defectosResultadoVisita;
	}

	public void setDefectosResultadoVisita(String defectosResultadoVisita) {
		this.defectosResultadoVisita = defectosResultadoVisita;
	}
}
