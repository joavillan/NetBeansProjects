package momatools.model.creation;

public class MatrizATR {
	private AtributosEquipos atributosEquipos;
	private DiscriminacionesHorarias discriminacionesHorarias;
	private String controladorPotencia;
	private String intensidadICP;
	private Potencias potencias;
	private Intensidades intensidades;
	public AtributosEquipos getAtributosEquipos() {
		return atributosEquipos;
	}
	public void setAtributosEquipos(AtributosEquipos atributosEquipos) {
		this.atributosEquipos = atributosEquipos;
	}
	public DiscriminacionesHorarias getDiscriminacionesHorarias() {
		return discriminacionesHorarias;
	}
	public void setDiscriminacionesHorarias(DiscriminacionesHorarias discriminacionesHorarias) {
		this.discriminacionesHorarias = discriminacionesHorarias;
	}
	public String getControladorPotencia() {
		return controladorPotencia;
	}
	public void setControladorPotencia(String controladorPotencia) {
		this.controladorPotencia = controladorPotencia;
	}
	public String getIntensidadICP() {
		return intensidadICP;
	}
	public void setIntensidadICP(String intensidadICP) {
		this.intensidadICP = intensidadICP;
	}
	public Potencias getPotencias() {
		return potencias;
	}
	public void setPotencias(Potencias potencias) {
		this.potencias = potencias;
	}
	public Intensidades getIntensidades() {
		return intensidades;
	}
	public void setIntensidades(Intensidades intensidades) {
		this.intensidades = intensidades;
	}
	
	
}
